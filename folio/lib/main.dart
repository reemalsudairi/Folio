import 'package:cloud_firestore/cloud_firestore.dart'; // Import Firestore
import 'package:firebase_auth/firebase_auth.dart';
import 'package:firebase_core/firebase_core.dart';
import 'package:flutter/material.dart';
import 'package:folio/firebase_options.dart'; // Import the generated firebase_options.dart
import 'package:folio/screens/homePage.dart';
import 'package:folio/screens/login.dart';
import 'package:folio/screens/splash.dart';


void main() async {
  WidgetsFlutterBinding.ensureInitialized();
  await Firebase.initializeApp(
    options: DefaultFirebaseOptions.currentPlatform, // Use the default Firebase options generated by flutterfire_cli
  );
  runApp(const MyApp());
}

class MyApp extends StatefulWidget {
  const MyApp({super.key});

  @override
  State<MyApp> createState() => _MyAppState();
}

class _MyAppState extends State<MyApp> {
  User? user;
  bool isBanned = false; // Track if the user is banned
  late Stream<DocumentSnapshot<Map<String, dynamic>>>? bannedStream;

  @override
  void initState() {
    super.initState();
    user = FirebaseAuth.instance.currentUser;
    if (user != null) {
      // Set up the real-time listener
      bannedStream = FirebaseFirestore.instance
          .collection('reader')
          .doc(user!.uid)
          .snapshots();
    }
  }

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      debugShowCheckedModeBanner: false,
      theme: ThemeData(
        colorScheme: ColorScheme.fromSeed(seedColor: const Color(0xFFF790AD)),
        useMaterial3: true,
        fontFamily: 'YoungSerif-Regular'
      ),
      home: user == null
          ? const Splash()
          : StreamBuilder<DocumentSnapshot<Map<String, dynamic>>>(
              stream: bannedStream,
              builder: (context, snapshot) {
                if (snapshot.hasData) {
                  final data = snapshot.data?.data();
                  if (data != null && data['banned'] == true) {
                    return BannedMessagePage();
                  }
                  return HomePage(userId: user!.uid);
                } else if (snapshot.hasError) {
                  return const Center(
                    child: Text(
                      'Error loading data. Please try again.',
                      style: TextStyle(color: Colors.red),
                    ),
                  );
                }
                return const Center(child: CircularProgressIndicator());
              },
            ),
    );
  }
}

